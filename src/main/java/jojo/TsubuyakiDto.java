package jojo;

public class TsubuyakiDto {

	private int id;
	private String speak;
	private String dt;
	
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the speak
	 */
	public String getSpeak() {
		return speak;
	}
	/**
	 * @param speak the speak to set
	 */
	public void setSpeak(String speak) {
		this.speak = speak;
	}
	/**
	 * @return the dt
	 */
	public String getDt() {
		return dt;
	}
	/**
	 * @param dt the dt to set
	 */
	public void setDt(String dt) {
		this.dt = dt;
	}
	
}
 